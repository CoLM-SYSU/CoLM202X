            call acc1d (r_fm    , a_fm    )
            call acc1d (r_fh    , a_fh    )
            call acc1d (r_fq    , a_fq    )

            call acc1d (r_us10m, a_us10m)
            call acc1d (r_vs10m, a_vs10m)
            call acc1d (r_fm10m, a_fm10m)

            deallocate (r_ustar )
            deallocate (r_ustar2) !Shaofeng, 2023.05.20
            deallocate (r_tstar )
            deallocate (r_qstar )
            deallocate (r_zol   )
